using System.Collections.Generic;
using UnityEngine;

public class CreatureCollisionProfile : MonoBehaviour















{















    static readonly List<CreatureCollisionProfile> ActiveProfiles = new();































    [SerializeField] string collisionGroupKey = string.Empty;















    [SerializeField] bool ignoreCollisionsWithinGroup = false;































    Collider[] cachedColliders = System.Array.Empty<Collider>();































    public string CollisionGroupKey => collisionGroupKey;















    public bool IgnoreCollisionsWithinGroup => ignoreCollisionsWithinGroup;































    void Awake()















    {















        CacheColliders();















    }































    void OnEnable()















    {















        CacheColliders();















        if (!ActiveProfiles.Contains(this))















            ActiveProfiles.Add(this);















        RefreshAllCollisions();















    }































    void OnDisable()















    {















        for (int i = 0; i < ActiveProfiles.Count; i++)















        {















            CreatureCollisionProfile other = ActiveProfiles[i];















            if (other == null || other == this)















                continue;















            ApplyIgnoreRule(this, other, false);















        }































        ActiveProfiles.Remove(this);















    }































    public void ApplyDefaults(string defaultGroupKey)















    {















        CacheColliders();















        if (string.IsNullOrWhiteSpace(collisionGroupKey))















            collisionGroupKey = defaultGroupKey;















        RefreshAllCollisions();















    }































    public void ConfigureGroup(string groupKey, bool ignoreWithinGroup)















    {















        CacheColliders();















        collisionGroupKey = groupKey ?? string.Empty;















        ignoreCollisionsWithinGroup = ignoreWithinGroup;















        RefreshAllCollisions();















    }































    void CacheColliders()















    {















        cachedColliders = GetComponentsInChildren<Collider>(true);















    }































    static void RefreshAllCollisions()















    {















        for (int i = 0; i < ActiveProfiles.Count; i++)















        {















            CreatureCollisionProfile a = ActiveProfiles[i];















            if (a == null)















                continue;































            a.CacheColliders();















            for (int j = i + 1; j < ActiveProfiles.Count; j++)















            {















                CreatureCollisionProfile b = ActiveProfiles[j];















                if (b == null)















                    continue;































                b.CacheColliders();















                ApplyIgnoreRule(a, b, ShouldIgnoreBetween(a, b));















            }















        }















    }































    static bool ShouldIgnoreBetween(CreatureCollisionProfile a, CreatureCollisionProfile b)















    {















        if (a == null || b == null)















            return false;















        if (!a.isActiveAndEnabled || !b.isActiveAndEnabled)















            return false;















        if (string.IsNullOrWhiteSpace(a.collisionGroupKey) || string.IsNullOrWhiteSpace(b.collisionGroupKey))















            return false;















        if (!string.Equals(a.collisionGroupKey, b.collisionGroupKey, System.StringComparison.Ordinal))















            return false;































        return a.ignoreCollisionsWithinGroup || b.ignoreCollisionsWithinGroup;















    }































    static void ApplyIgnoreRule(CreatureCollisionProfile a, CreatureCollisionProfile b, bool ignore)















    {















        if (a == null || b == null || a.cachedColliders == null || b.cachedColliders == null)















            return;































        for (int i = 0; i < a.cachedColliders.Length; i++)















        {















            Collider colA = a.cachedColliders[i];















            if (colA == null)















                continue;































            for (int j = 0; j < b.cachedColliders.Length; j++)















            {















                Collider colB = b.cachedColliders[j];















                if (colB == null)















                    continue;































                Physics.IgnoreCollision(colA, colB, ignore);















            }















        }















    }















}







